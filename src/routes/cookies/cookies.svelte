    <div class="ArticleBody" itemprop="articleBody">
      <h2>Ce este un cookie?</h2>
      <p>
        Un cookie este un fisier text de mici dimensiuni, format in general din identificatori, nume ale website-urilor
        litere si numere, caruia utilizatorul, cand navigheaza pe pagina web prin intermediul unui browser web (ex:
        Internet Explorer, Chrome, etc.), ii poate permite sau nu instalarea prin stocare pe computerul, terminalul
        mobil sau alte echipamente folosite de acesta. Fisierul cookie este “pasiv” (nu contine programe software,
        virusi sau spyware si nu poate accesa informatiile de pe hard disk-ul utilizatorului).
      </p>
      <h2>La ce sunt folosite cookies?</h2>
      <p>
        Aceste fisiere fac posibila recunoasterea terminalului utilizatorului si prezentarea catre acesta a unui
        continut adaptat preferintelor sale. Cookies asigura utilizatoriului o experienta placuta de navigare si sustin
        eforturile Gyo Cleaning pentru a oferi servicii confortabile utilizatorului, cum ar fi: preferintele in materie
        de confidentialitate online, cosul de cumparaturi sau publicitate relevanta. Cookies sunt utilizate in
        pregatirea unor statistici anonime agregate care ne ajuta sa intelegem modul in care un utilizator beneficiaza
        de paginile noastre web, permitandu-ne imbunatatirea structurii si continutului acestora.
      </p>
      <h2>Ce cookies folosim?</h2>
      <p>Folosim doua tipuri de cookies:</p>
      <ul>
        <li>
          de sesiune – fisiere temporare ce raman in terminalul utilizatorului pana la inchiderea sesiunii de navigare
          sau inchiderea aplicatiei (browser-ului web)
        </li>
        <li>
          persistente – fisiere care raman in terminalul utilizatorului pe o perioada definita de parametrii acelui
          cookie sau pana sunt sterse manual de utilizator.
        </li>
      </ul>
      <h2>Cum sunt folosite cookies de catre acest site?</h2>
      <p>
        O vizita pe acest site poate plasa urmatoarele tipuri de cookies, in functie de consimtamantul exprimat de
        utilizator:
