      <h2>Contin cookies date personale?</h2>
      <p>
        In cele mai multe cazuri, cookies nu identifica in mod direct utilizatorii de Internet, ci terminalul de pe care
        acestia au accesat anumite pagini web. Aceste date sunt criptate intr-un mod care nu permite accesul neautorizat
        al tertilor.
      </p>
      <h2>Retentia si stergerea cookies</h2>
      <p>
        In general, o aplicatie folosita pentru accesarea paginilor web permite salvarea cookies pe terminalul
        utilizatorului in mod implicit. Aceste setari pot fi modificate de catre utilizator astfel incat administrarea
        automata a cookies sa fie blocata de browser-ul web sau utilizatorul sa fie informat de fiecare data cand
        cookies sunt trimise catre terminalul sau.
      </p>
      <h2>Securitate si probleme legate de confidentialitate</h2>
      <p>
        Cookieurile NU sunt virusi! Ele folosesc formate tip plain text. Nu sunt alcatuite din bucati de cod asa ca nu
        pot fi executate nici nu pot auto-rula. In consecinta, nu se pot duplica sau replica pe alte retele pentru a se
        rula sau replica din nou. Deoarece nu pot indeplini aceste functii, nu pot fi considerate virusi.
      </p>
      <p>
        Cookie-urile pot fi totusi folosite pentru scopuri negative. Deoarece stocheaza informatii despre preferintele
        si istoricul de navigare al utilizatorilor, atat pe un anume site cat si pe mai multe alte siteuri, cookieurile
        pot fi folosite ca o forma de Spyware. Multe produse anti-spyware sunt constiente de acest fapt si in mod
        constant marcheaza cookie-urile pentru a fi sterse in cadrul procedurilor de stergere/scanare
        anti-virus/anti-spyware.
      </p>
      <p>
        In general browserele au integrate setari de confidentialitate care furnizeaza diferite nivele de acceptare a
        cookieurilor, perioada de valabilitate si stergere automata dupa ce utilizatorul a vizitat un anumit site.
      </p>
      <h2>Alte aspecte de securitate legate de cookie-uri</h2>
      <p>
        Deoarece protectia identitatii este foarte valoroasa si reprezinta dreptul fiecarui utilizator de internet, este
        indicat sa se stie ce eventuale probleme pot crea cookieurile. Pentru ca prin intermediul lor se transmit in mod
        constant in ambele sensuri informatii intre browser si website, daca un atacator sau persoana neautorizata
        intervine in parcursul de transmitere a datelor, informatiile continute de cookie pot fi interceptate.Desi
        foarte rar, acest lucru se poate intampla daca browserul se conecteaza la server folosind o retea necriptata
        (ex: o retea WiFi nesecurizata).
      </p>
      <p>
        Alte atacuri bazate pe cookie implica setari gresite ale cookieurilor pe servere. Daca un website nu solicita
        browserului sa foloseasca doar canale criptate, atacatorii pot folosi aceasta vulnerabilitate pentru a pacali
        browserele in a trimite informatii prin intermediul canalelor nesecurizate. Atacatorii utilizeaza apoi
        informatiile in scopuri de a accesa neautorizat anumite site-uri. Este foarte important sa fiti atenti in
        alegerea metodei celei mai potrivite de protectie a informatiilor personale.
      </p>
      <h2>Sfaturi pentru o navigare sigura si responsabila, bazata pe cookies.</h2>
      <p>
        Datorita flexibilitatii lor si a faptului ca majoritatea dintre cele mai vizitate site-uri si cele mai mari
        folosesc cookieuri, acestea sunt aproape inevitabile. Dezactivarea cookie-urilor nu va permite accesul
        utilizatorului pe site-urile cele mai raspandite si utilizate printre care Youtube, Gmail, Yahoo si altele.<br />
        Iata cateva sfaturi care va pot asigura ca navigati fara griji insa cu ajutorul cookieurilor:
      </p>
      <ul>
        <li>
          Particularizati-va setarile browserului in ceea ce priveste cookie-urile pentru a reflecta un nivel
          confortabil pentru voi al securitatii utilizarii cookie-urilor.
        </li>
        <li>
          Daca nu va deranjeaza cookie-urile si sunteti singura persoana care utilizeaza computerul, puteti seta termene
          lungi de expirare pentru stocarea istoricului de navigare si al datelor personale de acces.
        </li>
        <li>
          Daca impartiti accesul la calculator, puteti lua in considerare setarea browserului pentru a sterge datele
          individuale de navigare de fiecare data cand inchideti browserul. Aceasta este o varianta de a accesa
          site-urile care plaseaza cookieuri si de a sterge orice informatie de vizitare la inchiderea sesiunii
          navigare.
        </li>
        <li>Instalati-va si updatati-va constant aplicatii antispyware.</li>
        <li>
          Multe dintre aplicatiile de detectare si prevenire a spyware-ului includ detectarea atacurilor pe site-uri.
          Astfel, impiedica browserul de la a accesa website-uri care ar putea sa exploateze vulnerabilitatile
          browserului sau sa descarce software periculos.
        </li>
        <li>Asigurati-va ca aveti browserul mereu updatat.</li>
      </ul>
      <p>
        Cookie-urile sunt pretutindeni si nu pot fi evitate daca doriti sa va bucurati de acces pe cele mai bune si cele
        mai mari site-uri de pe Internet – locale sau internationale. Cu o intelegere clara a modului lor de operare si
        a beneficiilor pe care le aduc, puteti lua masurile necesare de securitate astel incat sa puteti naviga cu
        incredere pe internet.Dezactivarea si refuzul de a primi cookie-uri pot face anumite site-uri impracticabile sau
        dificil de vizitat si folosit. De asemenea, refuzul de a accepta cookie-uri nu inseamna ca nu veti mai
        primi/vedea publicitate online.Este posibila setarea din browser pentru ca aceste cookie-uri sa nu mai fie
        acceptate sau poti seta browserul sa accepte cookie-uri de la un site anume. Dar, de exemplu, daca nu esti
        inregistat folosind cookie-urile, nu vei putea lasa comentarii.
      </p>
